void	ft_putchar(char c);
int		ft_strcmp(char *s1, char *s2);
void	ft_print(int argc, char **argv);

int		main(int argc, char **argv)
{
	int		i;
	int		j;
	char	*tmp;

	i = 1;
	while (i < argc - 1)
	{
		j = 1;
		while (j < argc - i)
		{
			if (ft_strcmp(argv[j], argv[j + 1]) > 0)
			{
				tmp = argv[j];
				argv[j] = argv[j + 1];
				argv[j + 1] = tmp;
			}
			j = j + 1;
		}
		i = i + 1;
	}
	ft_print(argc, argv);
	return (0);
}

int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] == s2[i])
	{
		i = i + 1;
		if (s1[i] == '\0' || s2[i] == '\0')
			break ;
	}
	return (s1[i] - s2[i]);
}

void	ft_print(int argc, char **argv)
{
	int i;
	int j;

	i = 1;
	while (i < argc)
	{
		j = 0;
		while (argv[i][j] != '\0')
		{
			ft_putchar(argv[i][j]);
			j = j + 1;
		}
		ft_putchar('\n');
		i = i + 1;
	}
}
