int	ft_sqrt(int nb)
{
	int i;
	int bord;

	if (nb <= 0)
		return (0);
	i = 0;
	bord = 65536;
	if (nb <= 100)
		bord = 10;
	while (i < bord)
	{
		if (i * i == nb)
			return (i);
		i = i + 1;
	}
	return (0);
}
