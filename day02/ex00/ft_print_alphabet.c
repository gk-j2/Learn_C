int		ft_putchar(char c);

void	ft_print_alphabet(void)
{
	char c;

	c = 'a';
	while ('a' <= c && c <= 'z')
	{
		ft_putchar(c);
		c = c + 1;
	}
	ft_putchar("\n");
}
