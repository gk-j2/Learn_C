char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			str[i] -= 65;
			str[i] += 42;
			str[i] = (str[i] % 26) + 65;
		}
		if (str[i] >= 97 && str[i] <= 122)
		{
			str[i] -= 97;
			str[i] += 42;
			str[i] = (str[i] % 26) + 97;
		}
		i++;
	}
	return (str);
}
