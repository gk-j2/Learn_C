int		ft_find_next_prime(int nb)
{
	int i;
	int ans;

	ans = 0;
	i = 2;
	while (1)
	{
		while (i < nb)
		{
			if (nb % i == 0)
				ans = ans + 1;
			i = i + 1;
		}
		if (ans == 0)
			return (nb);
		else
		{
			ans = 0;
			i = 2;
			nb = nb + 1;
		}
	}
}
